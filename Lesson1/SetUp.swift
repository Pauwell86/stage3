import Foundation
import UIKit

func setNewUser(userNumber: Int, userName: String, userAge: UInt) -> User {
    var user = User(name: userName)
    user.age = userAge
    
    var imageName = String(userNumber) + "_1"
    user.avatar = UIImage(named: imageName)
    
    for index in 2...5 {
        imageName = String(userNumber) + "_" + String(index)
        if let image = UIImage(named: imageName) {
            user.fotoArray.append(image)
        }
    }
    
    return user
}
